#字面量写法
print(100) #整数（int）
print(3.14) #浮点数（float）
print(True) #布尔值（bool）
print("Hello Python") #字符串（string）
print("----------------")
print(None) #空值（NoneType）

#变量----->Python是动态类型语言，一个变量可以存储不同类型数据（但在项目开发中一般只存储一种类型数据）
num = 114.1
print(num)

num = num + 1
print(num)

num = "OK"
print(num)

num = True
print(num)